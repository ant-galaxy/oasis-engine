/**
 * Enum for XR feature type.
 */
export enum XRFeatureType {
  /** Movement tracking */
  MovementTracking,
  /** Control camera parameters such as focal length, etc.*/
  CameraDevice,
  /** Anchor tracking */
  AnchorTracking,
  /** Image tracking */
  ImageTracking,
  /** Plane tracking */
  PlaneTracking,
  /** Hit test */
  HitTest
}
