/**
 * Enumerates some devices that can be tracked.(including status, posture and other information)
 */
export enum XRTrackedInputType {
  /** Controller */
  Controller,
  /** Left controller */
  LeftController,
  /** Right controller */
  RightController,
  /** Camera */
  Camera,
  /** Left camera */
  LeftCamera,
  /** Right camera */
  RightCamera,
  /** Head */
  LeftHand,
  /** Right hand */
  RightHand,
  /** Length of input type */
  Length
}
